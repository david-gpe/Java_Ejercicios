package estructuras;

public class Estructuras {

    public static void main(String[] args) {
        int [] miArreglo = new int[3];
        miArreglo[0] = 1;
        miArreglo[1] = 3;
        miArreglo[2] = 5;
            
        for (int i = 0; i < miArreglo.length; i++) {
            System.out.println("El numero es: "+ miArreglo[i]);
        }
             
    }
    
}
